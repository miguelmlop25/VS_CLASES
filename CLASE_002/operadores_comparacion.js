// igualdad débil ==
console.log("1 == 1", 1 == 1);
console.log("0 == /", 0 == ""); // Da true, aunque no sea el mismo tipo de dato.

// igualdad estricta ===
console.log(1 === 1);
console.log(0 === ""); // Revisa que sea el mismo tipo de dato

// desigualdad débil !=
console.log(1 != 2);
console.log(undefined != null); // Da false, porque es un tipado débil

// desigualdad débil !=
console.log(1 != 2);
console.log(undefined != null); // Da false, porque es un tipado débil